    int random = (int) (Math.random() * 100);
    int attempts = 10;
    int score = 100;

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int guess = Integer.parseInt(jTextField1.getText());
        // user will get 100 points on first attempt and 90 points on second attempt and
        // so on and so forth
        // number of attempts available is 10 if user is unable to guess the number in
        // 10 attempts then he/she will get 0 points
        // this process continues until the user guesses the number correctly or the
        // number of attempts are over
        if (guess == random) {
            jLabel9.setText(" Congratulations! Your score is " + score);
            jButton1.setEnabled(false);
            jButton2.setEnabled(false);
        } else {
            while (attempts > 0) {
                if (guess > random) {
                    jLabel9.setText("Your guess is too high");
                    score = score - 10;
                    attempts--;
                    jLabel5.setText("Attempts left: " + attempts);
                    break;
                } else if (guess < random) {
                    jLabel9.setText("Your guess is too low");
                    score = score - 10;
                    attempts--;
                    jLabel5.setText("Attempts left: " + attempts);
                    break;
                }
            }
            if (attempts == 0) {
                jLabel9.setText("Better luck next time! Your score is 0");
                jLabel4.setText("The number was " + random);
                jButton1.setEnabled(false);
                jButton2.setEnabled(false);
            }
        }

    }// GEN-LAST:event_jButton1ActionPerformed


    int hint = 3;

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        // user have 3 hints available to guess the number correctly and each hint will
        // cost 10 points which will be deducted from the score
        // if user clicks on the hint button then the number of hints available will be
        // reduced by 1 and a JOptionPane will appear with the hint after a confirm
        // dialog box to confirm the deduction of 10 points from the score
        if (hint > 0) {
            hint--;
            int confirm = JOptionPane.showConfirmDialog(null,
                    "Are you sure you want to use a hint? 10 points will be deducted from your score", "Hint",
                    JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                score = score - 10;
                if (hint == 2) {
                    if (random % 2 == 0) {
                        JOptionPane.showMessageDialog(null, "The number is even");
                    } else {
                        JOptionPane.showMessageDialog(null, "The number is odd");
                    }
                } else if (hint == 1) {
                    if (random >= 0 && random <= 25) {
                        JOptionPane.showMessageDialog(null, "The number is between 0 and 25");
                    } else if (random >= 26 && random <= 50) {
                        JOptionPane.showMessageDialog(null, "The number is between 26 and 50");
                    } else if (random >= 51 && random <= 75) {
                        JOptionPane.showMessageDialog(null, "The number is between 51 and 75");
                    } else if (random >= 76 && random <= 100) {
                        JOptionPane.showMessageDialog(null, "The number is between 76 and 100");
                    }
                } else if (hint == 0) {
                    int flag = 0;
                    for (int i = 2; i <= random / 2; i++) {
                        if (random % i == 0) {
                            flag = 1;
                            break;
                        }
                    }
                    if (flag == 0) {
                        JOptionPane.showMessageDialog(null, "The number is prime");
                    } else {
                        JOptionPane.showMessageDialog(null, "The number is not prime");
                    }
                }
                jLabel2.setText("Hints left: " + hint);
            }
        } else {
            jLabel9.setText("You have no hints left");
        }

    }// GEN-LAST:event_jButton2ActionPerformed

